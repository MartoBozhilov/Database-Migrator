-- ============================================
-- PART 1: DATABASE SCHEMA
-- ============================================

-- Drop database if exists and create fresh
DROP DATABASE IF EXISTS university_system;
CREATE DATABASE university_system CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE university_system;

CREATE TABLE buildings (
    id INT AUTO_INCREMENT PRIMARY KEY,
    building_code VARCHAR(10) NOT NULL UNIQUE,
    building_name VARCHAR(100) NOT NULL,
    address VARCHAR(200) NOT NULL,
    construction_year SMALLINT NOT NULL,
    total_floors INT NOT NULL,
    has_elevator BOOLEAN DEFAULT FALSE,
    is_accessible BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_building_code (building_code)
);

CREATE TABLE classrooms (
    id INT AUTO_INCREMENT PRIMARY KEY,
    building_id INT NOT NULL,
    room_number VARCHAR(20) NOT NULL,
    capacity INT NOT NULL,
    room_type ENUM('LECTURE', 'LAB', 'SEMINAR', 'AUDITORIUM') NOT NULL,
    has_projector BOOLEAN DEFAULT FALSE,
    has_computers BOOLEAN DEFAULT FALSE,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY fk_classroom_building (building_id) REFERENCES buildings(id) ON DELETE RESTRICT,
    UNIQUE KEY uk_building_room (building_id, room_number),
    INDEX idx_room_type (room_type),
    INDEX idx_capacity (capacity)
);

CREATE TABLE departments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    department_code VARCHAR(10) NOT NULL UNIQUE,
    department_name VARCHAR(100) NOT NULL,
    building_id INT,
    office_room VARCHAR(20),
    phone VARCHAR(15),
    email VARCHAR(100),
    budget DECIMAL(12, 2) DEFAULT 0.00,
    established_date DATE NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY fk_dept_building (building_id) REFERENCES buildings(id) ON DELETE SET NULL,
    INDEX idx_dept_code (department_code),
    INDEX idx_dept_name (department_name)
);

CREATE TABLE instructors (
    id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    middle_name VARCHAR(50),
    email VARCHAR(100) NOT NULL UNIQUE,
    phone VARCHAR(15),
    fax_number VARCHAR(15),
    department_id INT NOT NULL,
    hire_date DATE NOT NULL,
    salary DECIMAL(10, 2) NOT NULL,
    university_rank ENUM('ASSISTANT', 'ASSOCIATE', 'FULL', 'EMERITUS') NOT NULL,
    is_tenured BOOLEAN DEFAULT FALSE,
    office_building_id INT,
    office_room VARCHAR(20),
    bio TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY fk_instructor_dept (department_id) REFERENCES departments(id) ON DELETE RESTRICT,
    FOREIGN KEY fk_instructor_building (office_building_id) REFERENCES buildings(id) ON DELETE SET NULL,
    INDEX idx_instructor_email (email),
    INDEX idx_instructor_dept (department_id),
    INDEX idx_instructor_name (last_name, first_name)
);

CREATE TABLE courses (
    id INT AUTO_INCREMENT PRIMARY KEY,
    course_code VARCHAR(20) NOT NULL UNIQUE,
    course_name VARCHAR(150) NOT NULL,
    description TEXT,
    credits INT NOT NULL,
    department_id INT NOT NULL,
    level ENUM('UNDERGRADUATE', 'GRADUATE', 'DOCTORATE') NOT NULL,
    max_enrollment INT DEFAULT 30,
    is_lab_required BOOLEAN DEFAULT FALSE,
    prerequisites VARCHAR(200),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY fk_course_dept (department_id) REFERENCES departments(id) ON DELETE RESTRICT,
    INDEX idx_course_code (course_code),
    INDEX idx_course_dept (department_id),
    INDEX idx_course_level (level)
);

CREATE TABLE students (
    id INT AUTO_INCREMENT PRIMARY KEY,
    student_number VARCHAR(20) NOT NULL UNIQUE,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    middle_name VARCHAR(50),
    email VARCHAR(100) NOT NULL UNIQUE,
    phone VARCHAR(15),
    date_of_birth DATE NOT NULL,
    gender ENUM('MALE', 'FEMALE', 'OTHER') NOT NULL,
    enrollment_date DATE NOT NULL,
    expected_graduation DATE,
    status ENUM('ACTIVE', 'INACTIVE', 'GRADUATED', 'SUSPENDED') NOT NULL DEFAULT 'ACTIVE',
    gpa DECIMAL(3, 2) DEFAULT 0.00,
    total_credits INT DEFAULT 0,
    address VARCHAR(200),
    city VARCHAR(50),
    state VARCHAR(50),
    zip_code VARCHAR(10),
    emergency_contact_name VARCHAR(100),
    emergency_contact_phone VARCHAR(15),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_student_number (student_number),
    INDEX idx_student_email (email),
    INDEX idx_student_name (last_name, first_name),
    INDEX idx_student_status (status)
);

CREATE TABLE schedules (
    id INT AUTO_INCREMENT PRIMARY KEY,
    course_id INT NOT NULL,
    instructor_id INT NOT NULL,
    classroom_id INT NOT NULL,
    semester ENUM('FALL', 'SPRING', 'SUMMER') NOT NULL,
    year SMALLINT NOT NULL,
    day_of_week ENUM('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY') NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    current_enrollment INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY fk_schedule_course (course_id) REFERENCES courses(id) ON DELETE CASCADE,
    FOREIGN KEY fk_schedule_instructor (instructor_id) REFERENCES instructors(id) ON DELETE RESTRICT,
    FOREIGN KEY fk_schedule_classroom (classroom_id) REFERENCES classrooms(id) ON DELETE RESTRICT,
    INDEX idx_schedule_semester (semester, year),
    INDEX idx_schedule_course (course_id),
    INDEX idx_schedule_instructor (instructor_id),
    INDEX idx_schedule_day_time (day_of_week, start_time)
);

CREATE TABLE enrollments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    student_id INT NOT NULL,
    schedule_id INT NOT NULL,
    enrollment_date DATE NOT NULL,
    enrollment_status ENUM('ENROLLED', 'DROPPED', 'COMPLETED', 'WITHDRAWN') NOT NULL DEFAULT 'ENROLLED',
    attendance_percentage DECIMAL(5, 2) DEFAULT 100.00,
    midterm_grade VARCHAR(2),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY fk_enrollment_student (student_id) REFERENCES students(id) ON DELETE CASCADE,
    FOREIGN KEY fk_enrollment_schedule (schedule_id) REFERENCES schedules(id) ON DELETE CASCADE,
    UNIQUE KEY uk_student_schedule (student_id, schedule_id),
    INDEX idx_enrollment_student (student_id),
    INDEX idx_enrollment_schedule (schedule_id),
    INDEX idx_enrollment_status (enrollment_status)
);

CREATE TABLE grades (
    id INT AUTO_INCREMENT PRIMARY KEY,
    enrollment_id INT NOT NULL UNIQUE,
    grade VARCHAR(2) NOT NULL,
    numeric_grade DECIMAL(5, 2),
    grade_points DECIMAL(3, 2),
    comments TEXT,
    graded_date DATE NOT NULL,
    graded_by INT NOT NULL,
    is_final BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY fk_grade_enrollment (enrollment_id) REFERENCES enrollments(id) ON DELETE CASCADE,
    FOREIGN KEY fk_grade_instructor (graded_by) REFERENCES instructors(id) ON DELETE RESTRICT,
    INDEX idx_grade_enrollment (enrollment_id),
    INDEX idx_grade_value (grade),
    INDEX idx_grade_date (graded_date)
);

CREATE TABLE student_clubs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    club_name VARCHAR(100) NOT NULL UNIQUE,
    club_code VARCHAR(20) NOT NULL UNIQUE,
    description TEXT,
    founded_date DATE NOT NULL,
    category ENUM('ACADEMIC', 'SPORTS', 'ARTS', 'SOCIAL', 'PROFESSIONAL', 'SERVICE') NOT NULL,
    advisor_id INT,
    meeting_day ENUM('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY', 'SUNDAY'),
    meeting_time TIME,
    meeting_location VARCHAR(100),
    budget DECIMAL(8, 2) DEFAULT 0.00,
    member_count INT DEFAULT 0,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY fk_club_advisor (advisor_id) REFERENCES instructors(id) ON DELETE SET NULL,
    INDEX idx_club_name (club_name),
    INDEX idx_club_category (category),
    INDEX idx_club_active (is_active)
);

CREATE TABLE club_memberships (
    id INT AUTO_INCREMENT PRIMARY KEY,
    student_id INT NOT NULL,
    club_id INT NOT NULL,
    join_date DATE NOT NULL,
    role ENUM('MEMBER', 'OFFICER', 'PRESIDENT', 'VICE_PRESIDENT', 'TREASURER', 'SECRETARY') NOT NULL DEFAULT 'MEMBER',
    is_active BOOLEAN DEFAULT TRUE,
    left_date DATE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY fk_membership_student (student_id) REFERENCES students(id) ON DELETE CASCADE,
    FOREIGN KEY fk_membership_club (club_id) REFERENCES student_clubs(id) ON DELETE CASCADE,
    UNIQUE KEY uk_student_club_active (student_id, club_id, is_active),
    INDEX idx_membership_student (student_id),
    INDEX idx_membership_club (club_id),
    INDEX idx_membership_role (role)
);

CREATE TABLE library_checkouts (
    id INT AUTO_INCREMENT PRIMARY KEY,
    student_id INT NOT NULL,
    book_isbn VARCHAR(20) NOT NULL,
    book_title VARCHAR(200) NOT NULL,
    author VARCHAR(100),
    checkout_date DATE NOT NULL,
    due_date DATE NOT NULL,
    return_date DATE,
    status ENUM('CHECKED_OUT', 'RETURNED', 'OVERDUE', 'LOST') NOT NULL DEFAULT 'CHECKED_OUT',
    fine_amount DECIMAL(6, 2) DEFAULT 0.00,
    librarian_notes TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
