-- ============================================
-- Create Views for Common Queries
-- ============================================

-- View: Active student enrollments with course details
CREATE VIEW v_active_enrollments AS
SELECT
    s.id AS student_id,
    s.student_number,
    CONCAT(s.first_name, ' ', s.last_name) AS student_name,
    c.course_code,
    c.course_name,
    c.credits,
    sc.semester,
    sc.year,
    CONCAT(i.first_name, ' ', i.last_name) AS instructor_name,
    e.enrollment_status
FROM enrollments e
JOIN students s ON e.student_id = s.id
JOIN schedules sc ON e.schedule_id = sc.id
JOIN courses c ON sc.course_id = c.id
JOIN instructors i ON sc.instructor_id = i.id
WHERE e.enrollment_status = 'ENROLLED';

-- View: Instructor teaching load
CREATE VIEW v_instructor_load AS
SELECT
    i.id AS instructor_id,
    CONCAT(i.first_name, ' ', i.last_name) AS instructor_name,
    d.department_name,
    COUNT(DISTINCT sc.id) AS courses_teaching,
    SUM(c.credits) AS total_credits,
    SUM(sc.current_enrollment) AS total_students
FROM instructors i
JOIN departments d ON i.department_id = d.id
LEFT JOIN schedules sc ON i.id = sc.instructor_id
LEFT JOIN courses c ON sc.course_id = c.id
GROUP BY i.id, instructor_name, d.department_name;

-- View: Student GPA calculation
CREATE VIEW v_student_gpa AS
SELECT
    s.id AS student_id,
    s.student_number,
    CONCAT(s.first_name, ' ', s.last_name) AS student_name,
    COUNT(g.id) AS completed_courses,
    SUM(c.credits) AS total_credits,
    AVG(g.grade_points) AS calculated_gpa
FROM students s
JOIN enrollments e ON s.id = e.student_id
JOIN grades g ON e.id = g.enrollment_id
JOIN schedules sc ON e.schedule_id = sc.id
JOIN courses c ON sc.course_id = c.id
WHERE g.is_final = TRUE
GROUP BY s.id, s.student_number, student_name;


